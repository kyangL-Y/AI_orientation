    private static CultureDocumentSection section(String title, String eyebrow, String... paragraphs)
    {
        return new CultureDocumentSection(title, eyebrow, Arrays.asList(paragraphs));
    }

    static class CultureDocumentSection
    {
        private final String title;
        private final String eyebrow;
        private final List<String> paragraphs;

        CultureDocumentSection(String title, String eyebrow, List<String> paragraphs)
        {
            this.title = title;
            this.eyebrow = eyebrow;
            this.paragraphs = paragraphs;
        }

        public String getTitle()
        {
            return title;
        }

        public String getEyebrow()
        {
            return eyebrow;
        }

        public List<String> getParagraphs()
        {
            return paragraphs;
        }
    }
